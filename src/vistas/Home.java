    private void JBeditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBeditarActionPerformed
        Funcionario funcionario = controlerF.editarFuncionario(JTcampoid.getText());

        JCBtipoid.setSelectedIndex(funcionario.getTipoDocumento()-1);
        JCBestadocivil.setSelectedIndex(funcionario.getEstadoCivil()-1);
        JTFnumid.setText(funcionario.getNumId());
        JTFnombres.setText(funcionario.getNombres());
        JTFapellidos.setText(funcionario.getApellidos());
        JCBsexo.setSelectedItem(funcionario.getSexo());
        JTFdireccion.setText(funcionario.getDireccion());
        JTFtelefono.setText(funcionario.getTelefono());
        JDCfechanacimiento.setDate(funcionario.getFechaNacimiento());
    }//GEN-LAST:event_JBeditarActionPerformed

